def draw_figure(coords):
    turtle.begin_fill()
    turtle.penup()
    for x, y in coords:
        turtle.goto(x, y)
        turtle.pendown()
    turtle.end_fill()


def main():
    # Set up the turtle window
    turtle.fillcolor('white')
    turtle.bgcolor('black')
    turtle.setup(800, 800)
    turtle.speed(2)
    turtle.color('white')

    # Draw the Apple logo and the leaf
    draw_figure(APPLE)
    draw_figure(LEAF)

    # Exit on click
    turtle.exitonclick()


if __name__ == '__main__':
    main()
